// Generates src/fontMetricFallbacks.generated.ts: metric-matched fallback
// @font-face rules for the built-in theme fonts in src/fonts.ts, so swapping
// from the fallback to the web font doesn't shift layout. Same technique as
// next/font's adjustFontFallback; formulas from
// https://developer.chrome.com/blog/font-fallbacks
// Run with: npm run generate-font-fallbacks

import { writeFileSync } from "node:fs";
import lora from "@capsizecss/metrics/lora";
import atkinsonHyperlegibleNext from "@capsizecss/metrics/atkinsonHyperlegibleNext";
import sometypeMono from "@capsizecss/metrics/sometypeMono";
import montserrat from "@capsizecss/metrics/montserrat";
import sourceSans3 from "@capsizecss/metrics/sourceSans3";
import arial from "@capsizecss/metrics/arial";
import georgia from "@capsizecss/metrics/georgia";
import courierNew from "@capsizecss/metrics/courierNew";

// When adding a font to src/fonts.ts, add its @capsizecss/metrics import here
// and regenerate. familyName in the metrics must match the FontConfig's
// fontFamily.
const webFonts = [
  lora,
  atkinsonHyperlegibleNext,
  sometypeMono,
  montserrat,
  sourceSans3,
];

const localFallbacks = {
  serif: georgia,
  monospace: courierNew,
  "sans-serif": arial,
};

const pct = (n) => `${(n * 100).toFixed(3)}%`;

function fallbackFace(web) {
  const local = localFallbacks[web.category] ?? arial;
  const sizeAdjust =
    web.xWidthAvg / web.unitsPerEm / (local.xWidthAvg / local.unitsPerEm);
  const adjustedEm = web.unitsPerEm * sizeAdjust;
  return `@font-face {
  font-family: '${web.familyName} Fallback';
  src: local('${local.familyName}');
  size-adjust: ${pct(sizeAdjust)};
  ascent-override: ${pct(web.ascent / adjustedEm)};
  descent-override: ${pct(Math.abs(web.descent) / adjustedEm)};
  line-gap-override: ${pct(web.lineGap / adjustedEm)};
}`;
}

const entries = webFonts
  .map((web) => `  ${JSON.stringify(web.familyName)}: ${JSON.stringify(fallbackFace(web))},`)
  .join("\n");

const output = `// Generated by scripts/generate-font-metric-fallbacks.mjs — do not edit.
// Regenerate with: npm run generate-font-fallbacks

// Metric-matched fallback @font-face rules, keyed by web font family name.
export const metricFallbackFaces: Record<string, string> = {
${entries}
};
`;

const outPath = new URL(
  "../src/fontMetricFallbacks.generated.ts",
  import.meta.url,
);
writeFileSync(outPath, output);
console.log(`Wrote ${webFonts.length} fallback faces to ${outPath.pathname}`);
